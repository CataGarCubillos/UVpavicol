    public class ManejadorRegistrosSalidas implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            try {
                long avesSacrificadas = Long.parseLong(txfAvesSacrificar.getText());
                LocalDate fecha = LocalDate.now();
                LocalTime hora = LocalTime.now();
            
                Salida salida = new Salida(avesSacrificadas, fecha, hora);
                
                Lote lote = (Lote) boxLote.getSelectedItem();
                lote.addSalidas(salida);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(PlantillaGestionarSalidas.this,ex.getMessage());  
            }
        }
    }
    public class ManejadorBorrar implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            txfAvesSacrificar.setText("");
            txfFecha.setText("");
            boxLote.setSelectedItem(null);
        }
    }
    
    public class LoteComboBoxModel implements ComboBoxModel<Lote>{
        private Object selected= null;// Objeto seleccionado en el JComboBox
        private Empresa empresa;
        
        //@Override
        public  LoteComboBoxModel(Empresa empresa) {
           this.empresa=empresa;//tipo objeto que recibe
        }
        
        @Override
        public void setSelectedItem(Object item) {
            this.selected = item;//seleccionar item
        }
        @Override
        public int getSize() {
            return empresa.getLosLotes().size();
        }
        @Override
        public Lote getElementAt(int index) {
            return empresa.getLosLotes().get(index);
        }
        @Override
        public Object getSelectedItem() {
            return this.selected;
        }
        @Override
        public void addListDataListener(ListDataListener l) {}
        @Override
        public void removeListDataListener(ListDataListener l) {}
    }
    
public class LotesModelo extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return empresa.getLosLotes().size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Lote loteParaTabla = empresa.getLosLotes().get(rowIndex);
            switch (columnIndex) {
                case 0:return loteParaTabla.getFechaIngreso();
                case 1: return loteParaTabla.getGalpon().getIdentificador();
                case 2: return loteParaTabla.getCantidadAves();
                case 3: return loteParaTabla.getIdentificador();
                case 4: return loteParaTabla.cantidadSAcrificadas();
                
                default:
                    throw new AssertionError();
            }
        }
        @Override
        public String getColumnName(int column) {
            switch (column) {
                case 0:return "fecha ingreso";
                case 1:return " galpon";
                case 2: return"cantidad aves";
                case 3: return "identificador lote";
                case 4: return "aves sacrificadas";
            }
            return "reza pq nc, como se ejecutaria esto";
        
        }
    }
